package Chapter1.LiteralInJava;

public class literal {
    public static void main(String[] args){
        //to store age
        byte age = 36;
        long agedino = 234567890l; // to store long integer value we use l or L
        char ch = 'A'; //always write in single quotes
        float f1 = 5.6f; // Double by deafult so make it float write f.
        double f2 = 4.66; // We can use d or D for declaring it as a double
        boolean a = true;// boolean
        boolean b = false; // boolean
        String name = "Aditya"; //String 
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(f2);
        System.out.println(agedino);
        System.out.println(a);
        System.out.println(b); 
        System.out.println(name); 
    }
}
