package Chapter4;
public class conditionals{
    public static void main(String[] args) {
        int age = 18;
        if(age>18){
            System.out.println("Yes boy you can drive!!!");
        }
        else if(age == 18){
            System.out.println("You are almost there!!");
        }
        else{
            System.out.println("No boy not yet!!");
        }
    }
}
//else block is optional